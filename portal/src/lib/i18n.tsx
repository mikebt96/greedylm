export type TranslationKey = keyof typeof translations.es;
export type Translations = { [K in TranslationKey]: (typeof translations.es)[K] extends readonly string[] ? readonly string[] : string };

// ── Context ───────────────────────────────────────────────────────────────────
interface LanguageContextValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: Translations;
}

const LanguageContext = createContext<LanguageContextValue>({
  lang: 'es',
  setLang: () => {},
  t: translations.es,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('es');

  // Load from localStorage on mount
  useEffect(() => {
    try {
      const saved = localStorage.getItem('greedylm_lang') as Lang | null;
      const valid: Lang[] = ['es', 'en', 'fr', 'zh', 'ru'];
      if (saved && valid.includes(saved)) {
        // eslint-disable-next-line react-hooks/set-state-in-effect
        setLangState(saved);
      }
    } catch {}
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem('greedylm_lang', l); } catch {}
  };

  return (
    <LanguageContext.Provider value={{ lang, setLang, t: translations[lang] }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useT() {
  return useContext(LanguageContext);
}
